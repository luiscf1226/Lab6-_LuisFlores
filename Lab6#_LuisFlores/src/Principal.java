    private void cbItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbItemStateChanged
        // TODO add your handling code here:
        DefaultComboBoxModel modelo = (DefaultComboBoxModel) cb.getModel();
        DefaultTableModel modelo2 = (DefaultTableModel) jTable1.getModel();
        if (evt.getStateChange() == 1) {

            Claudilist c = (Claudilist) modelo.getSelectedItem();

            for (Programas p : c.getLista()) {

                Object[] newRow = {
                    p.getNombre(),
                    p.getPuntuacion(),
                    p.getFecha(),
                    p.getTipo(),
                    p.getGenero()
                };
                modelo2.addRow(newRow);

            }

        }
    }//GEN-LAST:event_cbItemStateChanged

