    private void jButton5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton5MouseClicked
        // TODO add your handling code here:
        try {

            String name = a_nombre.getText();
            int p2 = (int) a_punt.getValue();
            String f = fecha1.getText();
            String tipo = "", genero = "";

            if (a.isSelected()) {
                tipo = "Anime";
            }
            if (s.isSelected()) {
                tipo = "Serie";
            }
            if (d.isSelected()) {
                tipo = "Documental";
            }
            if (p1.isSelected()) {
                tipo = "anime";
            }
            if (cb_genero.getSelectedIndex() == 0) {
                genero = "comedia";
            }
            if (cb_genero.getSelectedIndex() == 1) {
                genero = "romance";
            }
            if (cb_genero.getSelectedIndex() == 2) {
                genero = "terror";
            }
            if (cb_genero.getSelectedIndex() == 3) {
                genero = "Syfy";
            }
            if (cb_genero.getSelectedIndex() == 4) {
                genero = "Suspenso";
            }
            if (cb_genero.getSelectedIndex() == 5) {
                genero = "Accion";
            }
            DefaultComboBoxModel modelo = (DefaultComboBoxModel) cb.getModel();

            Programas p = new Programas(name, p2, f, tipo, genero);
//        

            cb.setModel(modelo);
            Claudilist c = new Claudilist();
            c.getLista().add(p);
            c.setNombre(cnombre.getText());
            modelo.addElement(c);
            cnombre.setText("");
            a_nombre.setText("");
            fecha1.setText("");
            
            JOptionPane.showMessageDialog(this, "La Claudilist ha sido creada exitosamente");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR AL CREAR");
        }
    }//GEN-LAST:event_jButton5MouseClicked

